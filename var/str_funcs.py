# str - string - базовый тип представляющий из себя неизменяемую последовательность символов

s = 'Hello World'

print(s.split())  # возвращает список подстрок (по умолчанию разделение по пробелу)
print(s.strip())  # используется для удаления пробелов и других символов из начала и конца строки (или любых других символов, указанных в аргументе метода)